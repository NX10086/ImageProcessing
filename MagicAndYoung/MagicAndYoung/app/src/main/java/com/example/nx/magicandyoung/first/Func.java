package com.example.nx.magicandyoung.first;

public class Func {
    private String name;
    private int imageId;
    public Func(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
